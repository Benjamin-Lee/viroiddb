<template>
  <div>
    <TheHeader>{{ $route.params.id }}</TheHeader>
    <div class="mt-5">
      <SeqTable :metadata="metadata"></SeqTable>
      <div class="grid grid-cols-1 lg:grid-cols-2 gap-8 mt-8">
        <div>
          <Card title="Circular Alignment" subtitle="Generated by CSA">
            <ClientOnly>
              <StorageImage
                class="w-full"
                :src="`cluster-csa/${identity}/Cls.${clusterNumber}-CircularAlignment.bmp`"
              />
            </ClientOnly>
          </Card>
        </div>
        <div>
          <Card
            title="Blocks"
            subtitle="Which aligned sequence regions are where"
          >
            <ClientOnly>
              <StorageImage
                class="w-full"
                :src="`cluster-csa/${identity}/Cls.${clusterNumber}-Blocks.bmp`"
              />
            </ClientOnly>
          </Card>
          <ClientOnly>
            <MsaView :clusterId="$route.params.id"></MsaView>
          </ClientOnly>
        </div>
      </div>
    </div>
  </div>
</template>
<script lang="ts">
import Vue from 'vue'
export default Vue.extend({
  data() {
    return { metadata: [{}] }
  },
  async fetch() {
    this.metadata = (
      await this.$fire.firestore
        .collection('sequences')
        .where('Cls.ID0.' + this.identity, '==', this.$route.params.id)

        .get()
    ).docs.map((doc) => doc.data())
  },
  computed: {
    clusterNumber(): string {
      return this.$route.params.id.split('.').pop() ?? ''
    },
    identity(): string {
      return this.$route.params.id.split('.').slice(-2, -1)[0].slice(2)
    },
  },
})
</script>
